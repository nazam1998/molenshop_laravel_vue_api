    {
        
        $order = Order::find($order);
        if ($order->user_id != Auth::id()) {
            return redirect()->back();
        }
        $orderdetails = OrderDetail::with('product')->where('order_id', $order->id)->get();
        return response()->json([
            'message' => "Succès",
            'data' => ['order' => $order, 'order_items' => $orderdetails],
            'status' => 200
        ]);
    }

